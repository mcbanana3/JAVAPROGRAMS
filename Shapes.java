package JAVACTOOOD;

public class Shapes {
	public static void main(String[] argv) {
		//Java Code for Area of Circle
		int radius = 10;
		double circle = 3.142 * (radius * radius);
		System.out.println("The Area of the Circle is : "+circle);
		//Java code for Area of Square
		int side = 10;
		int square = side * side;
		System.out.println("The Area of the Square is : "+square);
		//Java Code for Area of Rectangle
		int len = 10, bre = 15;
		int Area = len * bre;
		System.out.println("The Area of the Rectangle is : "+Area);
	}
}